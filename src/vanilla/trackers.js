/**
 * Creates a placeholder for blocked content
 * @param {HTMLIFrameElement} iframe The iframe element to block
 * @param {string} originalSrc The original source URL of the iframe
 * @returns {HTMLDivElement} The created wrapper element containing the placeholder
 */
export const createContentPlaceholder = (iframe, originalSrc) => {
  // Create a unique ID for the placeholder
  const placeholderId = `cookie-blocked-content-${Math.random()
    .toString(36)
    .substring(2, 11)}`;

  // Get the iframe's parent element
  const parentElement = iframe.parentElement;
  if (!parentElement) {
    throw new Error("Iframe has no parent element");
  }

  // Make the iframe invisible but keep it in place
  iframe.setAttribute("data-cookie-blocked", "true");
  iframe.setAttribute("data-original-src", originalSrc);
  iframe.src = "about:blank";

  // Create a wrapper div with position relative
  const wrapper = document.createElement("div");
  applyWrapperStyles(
    wrapper,
    iframe.style.width || "100%",
    iframe.style.height || "315px"
  );

  // Add content directly to the wrapper
  wrapper.innerHTML = createPlaceholderContent(placeholderId);

  // Create the placeholder for tracking purposes only
  const placeholderElement = document.createElement("div");
  placeholderElement.id = placeholderId;
  placeholderElement.className = "cookie-consent-blocked-iframe";
  placeholderElement.setAttribute("data-cookie-consent-placeholder", "true");
  placeholderElement.setAttribute("data-blocked-src", originalSrc);
  placeholderElement.style.display = "none"; // Hide the placeholder as we're not using it for display

  // Insert the wrapper right before the iframe
  parentElement.insertBefore(wrapper, iframe);

  // Position the iframe absolutely within the wrapper to prevent layout disruption
  positionIframeAbsolutely(iframe);

  // Move the iframe inside the wrapper
  wrapper.appendChild(iframe);

  // Add the hidden placeholder to the wrapper for tracking
  wrapper.appendChild(placeholderElement);

  // Add event listener to the button
  addSettingsButtonListeners(placeholderId);

  return wrapper;
};

/**
 * Ensures all placeholders remain visible (in case they get overwritten by scripts)
 */
export const ensurePlaceholdersVisible = () => {
  document
    .querySelectorAll('[data-cookie-blocked="true"]')
    .forEach((iframe) => {
      if (iframe instanceof HTMLIFrameElement) {
        const originalSrc = iframe.getAttribute("data-original-src");
        if (originalSrc && iframe.src !== "about:blank") {
          iframe.src = "about:blank";
        }

        // Make sure the iframe is still properly hidden
        positionIframeAbsolutely(iframe);

        // Make sure the parent wrapper is visible
        const wrapper = iframe.parentElement;
        if (wrapper && wrapper.style.display === "none") {
          wrapper.style.display = "flex";
        }
      }
    });
};

/**
 * Blocks tracking scripts and iframes based on keywords
 * @param {string[]} trackingKeywords Array of keywords to block
 * @returns {MutationObserver} MutationObserver that watches for new elements
 */
export const blockTrackingScripts = (trackingKeywords) => {
  // Remove all script tags that match tracking domains
  document.querySelectorAll("script").forEach((script) => {
    if (
      script.src &&
      trackingKeywords.some((keyword) => script.src.includes(keyword))
    ) {
      script.remove();
    }
  });

  // Also block iframes from tracking domains (especially for YouTube embeds)
  document.querySelectorAll("iframe").forEach((iframe) => {
    if (
      iframe.src &&
      trackingKeywords.some((keyword) => iframe.src.includes(keyword))
    ) {
      createContentPlaceholder(iframe, iframe.src);
    }
  });

  // Prevent new tracking scripts and iframes from being injected
  const observer = new MutationObserver((mutations) => {
    mutations.forEach((mutation) => {
      mutation.addedNodes.forEach((node) => {
        // Handle script tags
        if (node instanceof HTMLElement && node.tagName === "SCRIPT") {
          const src = node.getAttribute("src");
          if (
            src &&
            trackingKeywords.some((keyword) => src.includes(keyword))
          ) {
            node.remove();
          }
        }

        // Handle iframe tags (especially YouTube)
        if (node instanceof HTMLElement && node.tagName === "IFRAME") {
          const src = node.getAttribute("src");
          if (
            src &&
            trackingKeywords.some((keyword) => src.includes(keyword))
          ) {
            createContentPlaceholder(node, src);
          }
        }
      });
    });
  });

  observer.observe(document.documentElement, {
    childList: true,
    subtree: true,
  });

  return observer;
};

/**
 * Main cookie blocking manager that handles all aspects of cookie blocking
 */
export class CookieBlockingManager {
  constructor() {
    this.observerRef = null;
    this.intervalId = null;
  }

  /**
   * Initializes cookie blocking based on user preferences
   * @param {string[]} blockedHosts Array of hosts to block
   * @param {string[]} blockedKeywords Array of keywords to block in scripts and iframes
   */
  initialize(blockedHosts, blockedKeywords) {
    // Block network requests
    if (blockedHosts.length > 0) {
      console.debug(
        "[CookieKit] Blocking tracking requests for domains:",
        blockedHosts
      );
      blockTrackingRequests(blockedHosts);
    }

    // Block scripts and iframes
    if (blockedKeywords.length > 0) {
      console.debug(
        "[CookieKit] Blocking tracking scripts and iframes for keywords:",
        blockedKeywords
      );
      this.observerRef = blockTrackingScripts(blockedKeywords);

      // Set up periodic check to ensure placeholders remain visible
      this.startPlaceholderVisibilityCheck();
    }
  }

  /**
   * Starts a periodic check to ensure placeholders remain visible
   */
  startPlaceholderVisibilityCheck() {
    // Run the check immediately
    ensurePlaceholdersVisible();

    // Then set up an interval
    this.intervalId = window.setInterval(ensurePlaceholdersVisible, 2000);
  }

  /**
   * Cleans up all cookie blocking functionality
   */
  cleanup() {
    // Restore original request functions
    restoreOriginalRequests();

    // Disconnect observer
    if (this.observerRef) {
      this.observerRef.disconnect();
      this.observerRef = null;
    }

    // Clear interval
    if (this.intervalId !== null) {
      window.clearInterval(this.intervalId);
      this.intervalId = null;
    }
  }
}
