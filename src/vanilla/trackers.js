let originalXhrOpen = null;
let originalFetch = null;

/**
 * Gets the list of hosts to block based on user preferences
 * @param {Object|null} preferences - User cookie preferences
 * @returns {string[]} Array of hosts to block
 */
export const getBlockedHosts = (preferences) => {
  if (!preferences) {
    // If no preferences set, block everything
    return Object.values(trackers.categories).flat();
  }

  const blockedHosts = [];

  // Add hosts based on declined categories
  if (!preferences.Analytics) {
    blockedHosts.push(...trackers.categories.Analytics);
  }
  if (!preferences.Social) {
    blockedHosts.push(...trackers.categories.Social);
  }
  if (!preferences.Advertising) {
    blockedHosts.push(...trackers.categories.Advertising);
  }

  return [...new Set(blockedHosts)]; // Remove duplicates
};

/**
 * Gets the list of keywords to block based on user preferences
 * @param {Object|null} preferences - User cookie preferences
 * @returns {string[]} Array of keywords to block
 */
export const getBlockedKeywords = (preferences) => {
  if (!preferences) {
    // If no preferences set, block everything
    return Object.values(trackers.categories)
      .flat()
      .map((host) => host.replace(/\.[^.]+$/, ""));
  }

  const blockedHosts = getBlockedHosts(preferences);
  // Convert hosts to keywords by removing the TLD
  const keywords = [
    ...new Set(blockedHosts.map((host) => host.replace(/\.[^.]+$/, ""))),
  ];

  return keywords;
};

/**
 * Blocks network requests to specified domains by overriding XMLHttpRequest and fetch
 * @param {string[]} blockedHosts Array of domain strings to block
 */
export const blockTrackingRequests = (blockedHosts) => {
  // Store original functions if not already stored
  if (!originalXhrOpen) {
    originalXhrOpen = XMLHttpRequest.prototype.open;
  }
  if (!originalFetch) {
    originalFetch = window.fetch;
  }

  // Override XMLHttpRequest to block requests to tracking domains
  XMLHttpRequest.prototype.open = function (method, url) {
    const urlString = url.toString();
    if (blockedHosts.some((host) => urlString.includes(host))) {
      console.debug(`[CookieKit] Blocked XMLHttpRequest to: ${urlString}`);
      throw new Error(`Request to ${urlString} blocked by consent settings`);
    }
    return originalXhrOpen.apply(this, arguments);
  };

  // Override fetch API to block tracking requests
  window.fetch = function (url, options) {
    const urlString = url.toString();
    if (
      typeof urlString === "string" &&
      blockedHosts.some((host) => urlString.includes(host))
    ) {
      console.debug(`[CookieKit] Blocked fetch request to: ${urlString}`);
      return Promise.resolve(
        new Response(null, {
          status: 403,
          statusText: "Blocked by consent settings",
        })
      );
    }
    return originalFetch.apply(this, arguments);
  };
};

/**
 * Restores the original XMLHttpRequest and fetch implementations
 */
export const restoreOriginalRequests = () => {
  if (originalXhrOpen) {
    XMLHttpRequest.prototype.open = originalXhrOpen;
  }
  if (originalFetch) {
    window.fetch = originalFetch;
  }
};

